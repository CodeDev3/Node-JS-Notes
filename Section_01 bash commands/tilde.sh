# The tilde (~) in Bash (and other Unix-like shells) is a shortcut that represents your home directory.

# 🏠 What it means:

# ~ = /Users/your-username (on macOS)
# For example, if your username is divyankpandey, then:

# ~  →  /Users/divyankpandey

# 🧠 Why it’s useful:

# You don’t need to type the full path to your home directory — ~ is a fast shortcut.

# 📍 Examples:
# Command	What it does
# cd ~	Go to your home directory
# cd ~/Desktop	Go to the Desktop folder inside your home
# ls ~	List files in your home directory
# cp myfile.txt ~/Documents/	Copy file to your Documents folder
# echo ~	Print your home directory path